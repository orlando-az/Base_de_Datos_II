-- Ejercicio 1 – Promedio de precios
-- Enunciado: Calcular el precio promedio de todos los productos y 
-- mostrar aquellos que tienen un precio mayor al promedio, incluyendo su ID, nombre y precio.

-- Ejercicio 2 – Empleados activos
-- Enunciado: Identificar los empleados que aún se encuentran activos (sin fecha de finalización). 
-- Mostrar su ID, la fecha de inicio y la cantidad de días trabajados hasta la fecha actual.

-- Ejercicio 3 – Ventas por año
-- Enunciado: Calcular el total de ventas realizadas en cada año. 
-- Mostrar el año junto con el monto total de ventas, ordenado de mayor a menor.

-- Ejercicio 4 – Clientes frecuentes
-- Enunciado: Calcular cuántas órdenes ha realizado cada cliente y mostrar solo aquellos que tienen más de una orden, 
-- incluyendo el nombre del cliente y la cantidad de órdenes.

-- Ejercicio 5 – Productos sin ventas
-- Enunciado: Utilizar un CTE para identificar los productos que nunca fueron vendidos. 
-- Mostrar su ID, nombre y la categoría a la que pertenecen.